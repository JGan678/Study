public class Point {
    public int x = 0;
    public int y = 0;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setX(int rx) {
        x = rx;
    }

    public int getX() {
        return x;
    }

    public void setY(int ry) {
        y = ry;
    }

    public int getY() {
        return y;
    }
}